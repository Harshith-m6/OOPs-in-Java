package com.rcb.Inheritance;

public class Student {
	
	private int stuId;
	private String stuName;
	
	public void setStuId(int id)
	{
		stuId = id;
	}

	public int getStuId()
	{
		return stuId;
	}
	
	public void setStuName(String name)
	{
		stuName = name;
	}
	
	public String getStuName()
	{
		return stuName;
	}
}
